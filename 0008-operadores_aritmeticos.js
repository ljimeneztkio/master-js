/**
 * Los operadores aritmeticos me permiten realizar operaciones matematicas basicas
 * como suma, resta, multiplicación, división, exponente, incremento, decremento
 * (+, -, *, /, **, %, ++, --)
 */


let a, b, c, d, e;

a = 10;
b = 20;
c = 30;
d = 40;
e = 50;

// Suma
console.log(a + b); // 10  + 20 = 30

// Reste
console.log(b - c); // 20 - 30 = -10

// Multiplicacion
console.log(c * d); // 30 * 40 = 1200

// Division
console.log(d / e); // 40 / 50 = 0.8

// Potencia
console.log(a ** 2); // 10 ** 2 = 100

// Modulo
console.log(b % c); // 20 % 30 = 20
console.log(c % b); // 20 % 30 = 10 !El orden importa
